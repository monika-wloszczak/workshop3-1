package classes;

public class Student {
	int studentId;
	String name;
	String lastName;
	int age;
	String email;
	int groupId;
	public Student(int studentId, String name, String lastName, int age, String email, int groupId){
		this.studentId=studentId;
		this.name=name;
		this.lastName=lastName;
		this.age=age;
		this.email=email;
		this.groupId=groupId;
	}
}
